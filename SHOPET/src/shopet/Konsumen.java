    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        
         /*
        Button search untuk mencari data yang diinginkan secara cepat
        pencarian berdasarkan IDCUSTOMER dan Nama Customer
        */
         
            try {
                int row = tabelCust.getRowCount();
                for(int a= 0; a<row;a++){
                    model.removeRow(0);
                }
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/shopet2","root","");
                ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM customer where IDCustomer like '%" +
                                                                 txtCari.getText()+"%'" + "or NameCust like '%" + 
                                                                 txtCari.getText() + "%'");
                while(rs.next()) {
                    String data [] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)};
                    model.addRow(data);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Karyawan.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_searchActionPerformed

