    private void Tampil() {
        // untuk menambahkan baris pada tabel
        int row = tabelCust.getRowCount();
        for(int a= 0; a<row;a++){
            model.removeRow(0);
        }
           try {
               Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/shopet2","root","");
               ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM customer");
                while(rs.next()) {
                    String data [] = {rs.getString(1),rs.getString(2),rs.getString(4),rs.getString(3)};
                    model.addRow(data);}
            } catch (SQLException ex) {
               Logger.getLogger(Konsumen.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
